package com.iwinner.hibenate.model;

public class Employee {
	
	private String employeeId;
	private String employeeName;
	private int employeeSalary;
	private int employeedeptNumber;
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public int getEmployeeSalary() {
		return employeeSalary;
	}
	public void setEmployeeSalary(int employeeSalary) {
		this.employeeSalary = employeeSalary;
	}
	public int getEmployeedeptNumber() {
		return employeedeptNumber;
	}
	public void setEmployeedeptNumber(int employeedeptNumber) {
		this.employeedeptNumber = employeedeptNumber;
	}
	
}
